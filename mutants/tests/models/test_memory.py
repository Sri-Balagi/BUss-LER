def test_memory_valid():
    """Test full Memory model."""
    data = {
        "id": uuid4(),
        "twin_id": uuid4(),
        "title": "Full Memory",
        "content": "This memory has all fields.",
        "memory_category": MemoryCategory.REFLECTION,
        "source": MemorySource.DOCUMENT,
        "importance": Decimal("0.50"),
        "embedding_status": EmbeddingStatus.FAILED,
        "metadata": {},
        "created_at": datetime.now(UTC),
        "updated_at": datetime.now(UTC),
        "deleted_at": None,
    }
    model = Memory(**data)
    assert model.id == data["id"]
    assert model.twin_id == data["twin_id"]
    assert model.memory_category == MemoryCategory.REFLECTION
    assert model.embedding_status == EmbeddingStatus.FAILED
    assert model.deleted_at is None
