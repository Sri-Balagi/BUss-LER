from datetime import UTC, datetime
from decimal import Decimal
from uuid import uuid4

import pytest
from app.models.enums import EmbeddingStatus, MemoryCategory, MemorySource
from app.models.memory import Memory, MemoryCreate, MemoryUpdate
from pydantic import ValidationError


def test_memory_create_valid():
    """Test creating a valid MemoryCreate model."""
    data = {
        "title": "Vendor contract signed",
        "content": "The entity successfully signed the contract with Acme Corp.",
        "memory_category": MemoryCategory.EVENT,
        "source": MemorySource.EXECUTION,
        "importance": Decimal("0.85"),
        "metadata": {"vendor_id": "acme-123"},
    }
    model = MemoryCreate(**data)
    assert model.title == "Vendor contract signed"
    assert model.memory_category == MemoryCategory.EVENT
    assert model.source == MemorySource.EXECUTION
    assert model.content == "The entity successfully signed the contract with Acme Corp."
    assert model.importance == Decimal("0.85")
    assert model.embedding_status == EmbeddingStatus.PENDING
    assert model.metadata == {"vendor_id": "acme-123"}
    assert model.summary is None
    assert model.embedding_model is None


def test_memory_create_invalid_importance():
    """Test validation failure for importance out of bounds."""
    data = {
        "title": "Test Title",
        "content": "Test content",
        "memory_category": MemoryCategory.OBSERVATION,
        "source": MemorySource.OBSERVATION,
        "importance": Decimal("1.50"),
    }

    with pytest.raises(ValidationError) as exc:
        MemoryCreate(**data)
    assert "Input should be less than or equal to 1" in str(exc.value)

    data["importance"] = Decimal("-0.10")
    with pytest.raises(ValidationError) as exc:
        MemoryCreate(**data)
    assert "Input should be greater than or equal to 0" in str(exc.value)


